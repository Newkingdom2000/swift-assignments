// operators  Descripion 
// ==         equal to 
// !=         not equal to
// >          Greater than
// <          less than
// >=         Greater than or equal to
// <=         less than or equal to



// comparison operators

print(1 == 1) // equal to 
print(2 != 1) // not equal to 
print(2 > 1) // Greater than
print(1 < 2) // less than
print(1 >= 1) // Greater than or equal to
print(2 <= 1) // less or equal to